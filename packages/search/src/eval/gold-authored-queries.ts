/**
 * Authored gold queries — output of the stratified-template recipe (#344
 * step 2). Maintained as a separate file from `gold-standard-queries.ts`
 * so:
 *
 * 1. The `recipe-apply` CLI can codegen new entries here without
 *    interleaving with the mechanically-migrated legacy queries.
 * 2. Reviewers can audit authored coverage growth as a clean diff.
 * 3. When authored coverage replaces a legacy query, that's a delete in
 *    `gold-standard-queries.ts` — independent from the authoring stream.
 *
 * The exported `AUTHORED_QUERIES` is concatenated with the legacy set in
 * `gold-standard-queries.ts` to form the runtime `GOLD_STANDARD_QUERIES`.
 *
 * Workflow:
 *
 *   recipe-author --collection X --live --adversarial-filter ...
 *     -> /tmp/candidates.json
 *
 *   recipe-validate --candidates /tmp/candidates.json
 *                   --output /tmp/candidates.validated.json
 *                   --report /tmp/review.md
 *     -> enriched JSON with per-candidate validation labels
 *
 *   <human reviews report, edits enriched JSON to mark approvals>
 *
 *   recipe-apply --candidates /tmp/candidates.validated.json
 *     -> appends approved entries to AUTHORED_QUERIES below
 *
 * @see https://github.com/SgtPooki/wtfoc/issues/344
 */

